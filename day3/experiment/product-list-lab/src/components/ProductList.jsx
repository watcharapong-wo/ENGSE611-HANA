import React, { useState, useMemo } from 'react';
import PropTypes from 'prop-types';
import ProductCard from './ProductCard';
import './ProductList.css';

function ProductList({ products, categories, onAddToCart, onViewDetails }) {
    const [selectedCategory, setSelectedCategory] = useState('all');
    const [searchQuery, setSearchQuery] = useState('');
    const [sortBy, setSortBy] = useState('');

    // ‚úÖ ‡∏Å‡∏£‡∏≠‡∏á + ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ + ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö
    const filteredProducts = useMemo(() => {
        let result = selectedCategory === 'all' 
            ? products 
            : products.filter(p => p.category === selectedCategory);

        // üîç ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ (‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤ + description)
        if (searchQuery.trim() !== '') {
            const query = searchQuery.toLowerCase();
            result = result.filter(p =>
                p.name.toLowerCase().includes(query) ||
                p.description.toLowerCase().includes(query)
            );
        }

        // ‚ÜïÔ∏è ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö
        if (sortBy === 'name') {
            result = [...result].sort((a, b) => a.name.localeCompare(b.name));
        } else if (sortBy === 'price') {
            result = [...result].sort((a, b) => a.price - b.price);
        } else if (sortBy === 'rating') {
            result = [...result].sort((a, b) => b.rating - a.rating);
        }

        return result;
    }, [products, selectedCategory, searchQuery, sortBy]);

    return (
        <div className="product-list-container">
            {/* Header */}
            <div className="header">
                <h1>üõçÔ∏è ‡∏£‡πâ‡∏≤‡∏ô‡∏Ñ‡πâ‡∏≤‡∏≠‡∏≠‡∏ô‡πÑ‡∏•‡∏ô‡πå</h1>
                <p>Lab 3.2 - ‡∏Å‡∏≤‡∏£‡∏™‡∏£‡πâ‡∏≤‡∏á Components ‡πÅ‡∏•‡∏∞ Props</p>
            </div>

            {/* Filters */}
            <div style={{ marginBottom: '20px', textAlign: 'center' }}>
                <label htmlFor="category-select">‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà: </label>
                <select 
                    id="category-select"
                    value={selectedCategory}
                    onChange={(e) => setSelectedCategory(e.target.value)}
                    style={{ padding: '5px', marginRight: '10px' }}
                >
                    {categories.map(category => (
                        <option key={category.id} value={category.id}>
                            {category.name}
                        </option>
                    ))}
                </select>

                {/* üîç Search */}
                <input 
                    type="text"
                    placeholder="‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤..."
                    value={searchQuery}
                    onChange={(e) => setSearchQuery(e.target.value)}
                    style={{ padding: '5px', marginRight: '10px' }}
                />

                {/* ‚ÜïÔ∏è Sort */}
                <select 
                    value={sortBy}
                    onChange={(e) => setSortBy(e.target.value)}
                    style={{ padding: '5px' }}
                >
                    <option value="">-- ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö --</option>
                    <option value="name">‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤</option>
                    <option value="price">‡∏£‡∏≤‡∏Ñ‡∏≤</option>
                    <option value="rating">Rating</option>
                </select>
            </div>

            {/* Products */}
            <div className="products-grid">
                {filteredProducts.map(product => (
                    <ProductCard
                        key={product.id}
                        product={product}
                        onAddToCart={onAddToCart}
                        onViewDetails={onViewDetails}
                    />
                ))}
            </div>
        </div>
    );
}

ProductList.propTypes = {
    products: PropTypes.array.isRequired,
    categories: PropTypes.array.isRequired,
    onAddToCart: PropTypes.func.isRequired,
    onViewDetails: PropTypes.func.isRequired
};

export default ProductList;
